// Função de busca otimizada
export function searchCIDCompleto(query: string): CidData[] {
  if (!query || query.length < 1) return []
  
  const searchTerm = query.toLowerCase()
  
  return CIDS_COMPLETO.filter(cid => 
    cid.codigo.toLowerCase().includes(searchTerm) ||
    cid.descricao.toLowerCase().includes(searchTerm)
  ).slice(0, 15) // Retorna até 15 resultados
}
